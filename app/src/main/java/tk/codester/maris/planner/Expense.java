package tk.codester.maris.planner;




public class Expense {

    String name, cost;


    public Expense(){
        this.name = name;
        this.cost = cost;
    }


    public void setName(String name) {
        this.name = name;
    }

    public void setCost(String cost) {
        this.cost = cost;
    }

    public String getName() {
        return name;
    }

    public String getCost() {
        return cost;
    }

}
